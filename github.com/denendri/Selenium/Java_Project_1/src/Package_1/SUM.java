package Package_1;

public class SUM {
	public static void main(String args[]){

			   int i, j, x;

			   for(i=1;i<=5;i++){

			     if (i<=3){

			      x=i;

			     }

			     else {

			      x=6-i;   

			     }
			 
			   for(j=1;j<2*x;j++){

			     System.out.print("*");

			    }
			     System.out.println("");

			   }

			 }


}
